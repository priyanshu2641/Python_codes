#Arithmatic Operators
a=15
b=4
print("Sum=",a+b)
print("Difference=",a-b)
print("Product=",a*b)
print("Division=",a/b)
print("Remainder=",a%b)
print("Quotient=",a//b)
print("a raised to the power of b is",a**b)

#Assignment Operators
a=5
print(a)
a+=6
print(a)
a/=2
print(a)

# Comparision Operator
a=10
print(a==10)
print(a>16)
print(a<=6)
print(a>=6)

# Logical Operators
a=True
b=False
print(a and b)
print(a or b)

# Identity Operators
a=9
b=10
print(a is b)
print(a is not b)

# Membership Operators
a=[26,4,2001,5,11,1988,254,183,94]
print(254 in a)
print(254 not in a)

# Bitwise Operator
# 0=00
# 1=01
# 2=10
# 3=11
print(0&2)
print(0|2)
print(1&4)